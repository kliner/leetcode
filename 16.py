class Solution:
	# @return an integer
	def threeSumClosest(self, num, target):
		num.sort()
		n = len(num)
		if n < 3:
			return 0
		res = 9999
		dist = 9999
		for i in range(n):
			j = i + 1
			k = n - 1 
			while j < k:
				s = num[i] + num[j] + num[k]
				if s == target:
					return target
				if abs(s - target) < dist:
					dist = abs(s - target)
					res = s
				if s < target:
					j += 1
				else:
					k -= 1
		return res

